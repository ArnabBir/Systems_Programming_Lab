TITLE A2_3:ASSIGNMENT_2_QUES_3
.MODEL SMALL
.STACK 100H
.DATA
    MSG DB 0DH,0AH,'$'
	ENTR DB 'ENTER AN INTEGER$'
	MSG2 DB 'SUM OF PRIME NUMBERS= $'
.CODE
MAIN PROC
        MOV AX,@DATA
        MOV DS,AX
  MOV AH,2
  MOV BH,2
  MOV DL,0
  W1:
	CMP BH,20
	JG W2
	MOV CH,2
	W3:
		CMP CH,BH
		JGE SUM
		MOV BL,BH
		W5:
			SUB BL,CH
			CMP BL,0
			JG W5
			JL W6
			JE W4
		W6:
			INC CH
			JMP W3
	SUM:
		ADD DL,BH
	W4:
		INC BH
		JMP W1
  W2:
	MOV BL,DL
	MOV CL,0
	W7:
		CMP BL,10
		JL END1
		INC CL
		SUB BL,10
		JMP W7
  END1:
	MOV AH,9
	LEA DX,MSG2
	INT 21H
	MOV AH,2
	MOV DL,CL
	ADD DL,48
	CMP DL,'0'
	JE W8
	INT 21H
	W8:
		MOV DL,BL
		ADD DL,48
		INT 21H
  MOV AH,4CH
  INT 21H
MAIN ENDP
END MAIN

        
