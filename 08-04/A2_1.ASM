TITLE A2_1:ASSIGNMENT_2_QUES_1
.MODEL SMALL
.STACK 100H
.DATA
    MSG DB 0DH,0AH,'$'
	ENTR DB 'ENTER AN INTEGER$'
	MSG2 DB 'SUM OF DIGITS= $'
.CODE
MAIN PROC
        MOV AX,@DATA
        MOV DS,AX
	MOV AH,9
	LEA DX,ENTR
	INT 21H
	LEA DX,MSG
	INT 21H
  MOV AH,1
  MOV CL,0
  MOV BL,0
  INT 21H
  W1:
	CMP AL,0DH
	JE W2
	AND AL,0FH
	ADD BL,AL
	INT 21H
	JMP W1
  W2:
	CMP BL,10
	JL END1
	INC CL
	SUB BL,10
	JMP W2
  END1:
	MOV AH,9
	LEA DX,MSG2
	INT 21H
	MOV AH,2
	MOV DL,CL
	ADD DL,48
	CMP DL,'0'
	JE W3
	INT 21H
	W3:
		MOV DL,BL
		ADD DL,48
		INT 21H
  MOV AH,4CH
  INT 21H
MAIN ENDP
END MAIN

        
