TITLE A2_2:ASSIGNMENT_2_QUES_2
.MODEL SMALL
.STACK 100H
.DATA
    MSG DB 0DH,0AH,'$'
	ENTR DB 'ENTER AN INTEGER$'
    MSG2 DB 'YES, IT IS PRIME$'
	MSG3 DB 'NO,IT IS NOT PRIME$'
.CODE
MAIN PROC
        MOV AX,@DATA
        MOV DS,AX
	MOV AH,9
	LEA DX,ENTR
	INT 21H
	LEA DX,MSG
	INT 21H
  MOV AH,1
  INT 21H
  MOV BH,AL
  SUB BH,48
  INT 21H
  MOV DL,AL
  CMP DL,0DH
  JE INIT
  SUB DL,48
  LOOP1:
		CMP BH,0
		JE W1
		ADD DL,10
		DEC BH
		JMP LOOP1
  W1:
	MOV BH,DL
	INIT:
	MOV AH,9
	CMP BH,1
	MOV DL,BH
	SHR DL,1
	JE NO
	MOV CH,2
	W3:
		CMP CH,DL
		JGE YES
		MOV BL,BH
		W5:
			SUB BL,CH
			CMP BL,0
			JG W5
			JL W6
			JE NO
		W6:
			INC CH
			JMP W3
  YES:
	LEA DX,MSG
	INT 21H
	LEA DX,MSG2
	INT 21H
	JMP END1	
  NO:
	LEA DX,MSG
	INT 21H
	LEA DX,MSG3
	INT 21H
  END1:
  MOV AH,4CH
  INT 21H
MAIN ENDP
END MAIN

        
