TITLE A3_3:ASSIGNMENT_3_QUES_3
.MODEL SMALL
.STACK 100H
.DATA
    MSG DB 0DH,0AH,'$'
	ENTR DB 'ENTER A HEX NUMBER$'
	MSG2 DB 'NUMBER IN HEX: $'
.CODE
MAIN PROC
        MOV AX,@DATA
        MOV DS,AX
	MOV AH,9
	LEA DX,ENTR
	INT 21H
	LEA DX,MSG
	INT 21H
	CALL INHEX
	MOV AH,9
	LEA DX,MSG2
	INT 21H
	CALL OUTHEX
	MOV AH,4CH
	INT 21H
MAIN ENDP


INCLUDE d:\INHEX.ASM
INCLUDE d:\OUTHEX.ASM
END MAIN